#include "ofApp.h"

//--------------------------------------------------------------
void ofApp::setup() {

	ofSetFrameRate(30);
	ofSetWindowTitle("openframeworks");

	ofBackground(239);
	ofEnableDepthTest();

	this->frame.setMode(ofPrimitiveMode::OF_PRIMITIVE_LINES);
}

//--------------------------------------------------------------
void ofApp::update() {

	ofSeedRandom(39);

	this->face.clear();
	this->frame.clear();

	int mod = 39;
	for (int x = 0; x < mod; x += 1) {

		for (int y = 0; y < mod; y += 1) {

			int num = (x * y + ofGetFrameNum()) % mod;
		
			auto location = glm::vec3((x * 10) - ((mod * 10) * 0.5) + 5, (y * 10) - ((mod * 10) * 0.5) + 5, 0);
			auto size = num > 0 ? ofMap(num, 0, mod, 10, 0) : 0;

			this->setBoxToMesh(this->face, this->frame, location, size, size, size);
		}
	}
}

//--------------------------------------------------------------
void ofApp::draw() {

	this->cam.begin();

	ofSetColor(39);
	this->face.draw();

	ofSetColor(239);
	this->frame.drawWireframe();
	
	this->cam.end();
}

//--------------------------------------------------------------
